function criaLista(array) {
  const info = document.getElementById("Personagens");
  info.innerHTML = "";
  for (let i = 0; i < array.length; i++) {
    info.innerHTML += `
      <section class="P000">
    <ul>
    <img src="Icones/${array[i].Nome}.png"alt="">
       <a href='${array[i].Link}' target="_blank" >${array[i].Nome}</a>   
       <li>${array[i].Titulo}</li> 
       <li>Lane: ${array[i].Lane}</li>
       <li>Criação: ${array[i].Criação}</li>        
    </ul>
    </section>
  `;
  }
}
